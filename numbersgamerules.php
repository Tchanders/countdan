<li>You will be given 6 numbers and a target number</li>
<li>You can choose to be given between 0 and 4 big numbers (25, 50, 75 or 100)</li>
<li>The rest will be small numbers (1 to 10)</li>
<li>Try to make the target number using the six randomly chosen numbers</li>
<li>You can use any combination of adding, subtracting, multiplying and/or dividing</li>
<li>You don't have to use all of the numbers</li>
<li>You can't use any number twice</li>
<li>You can use brackets</li>
<li>At no stage can you make a negative number or a fraction</li>
<li>It isn't always possible to make the target</li>