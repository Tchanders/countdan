<li>You will be given nine letters</li>
<li>You can choose whether each letter is a vowel or a consonant</li>
<li>Try to make the longest word possible using your letters</li>
<li>The word must be in the Oxford English Dictionary</li>
<li>The word can be a standard inflection, such as "writes" and "writing"</li>
<li>The word can't be a proper noun, such as "Venus"</li>
<li>The word can't be hyphenated</li>
<li>You don't have to use all the letters, but the more you use the better</li>
<li>You can't use any letter twice</li>
<li>The Countdan algorithm won't show any solutions less than five letters long</li>